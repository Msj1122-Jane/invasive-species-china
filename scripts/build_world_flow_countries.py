with open('data/species_341.json','r',encoding='utf-8') as f:
    species = json.load(f)

country_counts = Counter()
for sp in species:
    origin = sp.get('origin','').strip()
    countries = origin_map.get(origin, [])
    for c in countries:
        country_counts[c] += 1

# Build result
nodes = []
for c, count in country_counts.most_common():
    if c in coords and count > 0:
        nodes.append({'name': c, 'value': count, 'coord': coords[c]})

# Add China as target (aggregate all species)
if not any(n['name'] == '中国' for n in nodes):
    nodes.append({'name': '中国', 'value': 341, 'coord': [105, 35]})
else:
    for n in nodes:
        if n['name'] == '中国':
            n['value'] = 341

print(f'Total nodes: {len(nodes)}')
for n in nodes:
    print(f'  {n["name"]:15s} {n["value"]:3d}  {n["coord"]}')

# Save to JSON
with open('data/world_flow_countries.json','w',encoding='utf-8') as f:
    json.dump(nodes, f, ensure_ascii=False, indent=2)
print(f'\nSaved to data/world_flow_countries.json')
